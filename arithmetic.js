//This function takes two arguments, num1 and num2, and returns their sum. For example, if you call add(5, 3), it will return 8.
var add = function(num1,num2){
    return num1 + num2;
}
//This function also takes two arguments, num1 and num2, and returns the result of subtracting num2 from num1. For instance, if you call sub(5, 3), it will return 2.
var sub = function(num1,num2){
    return num1 - num2;
}
//This function multiplies num1 and num2 together. So, if you call mul(5, 3), it will return 15.
var mul = function(num1,num2){
    return num1 * num2;
}
//This function divides num1 by num2. If you call div(6, 3), it will return 2.
var div = function(num1,num2){
    return num1 / num2;
}
// These functions are all defined using JavaScript's function expression syntax,
// where the function keyword is followed by a name, a list of parameters in parentheses, and a block of code 
//enclosed in curly braces. The return statement specifies what value should be returned by the function.